/*
Essa estrura de código é para a ultilização das estruturas de 
repetição rais com:
-While;
-For; 
-For in*;

As estruturas de repetiçoes possuem 4 parte distintas:
-Inicio da variável;
-Condição para repetição;
-Corpo;
-Icrementação ou decrementação
*/
function contagem() {
  var x = 0;
  while (x <= 10) {
    console.log("Contei " + x);
    x = x + 1;
  }
}
